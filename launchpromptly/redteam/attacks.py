# ── Helpers ──────────────────────────────────────────────────────────────────

def _msg(content: str) -> List[Dict[str, str]]:
    return [{"role": "user", "content": content}]


def _with_system(system: str, user: str) -> List[Dict[str, str]]:
    return [
        {"role": "system", "content": system},
        {"role": "user", "content": user},
    ]


